    SWinbar_Concat((CD-1)*48+1:CD*48)=fluxW.SWinbar;
    SWoutbar_Concat((CD-1)*48+1:CD*48)=fluxW.SWoutbar;
    LWinbar_Concat((CD-1)*48+1:CD*48)=fluxW.LWinbar;
    LWoutbar_Concat((CD-1)*48+1:CD*48)=fluxW.LWoutbar;
    NetRs_Concat((CD-1)*48+1:CD*48)=fluxW.NetRs;
    NetRl_Concat((CD-1)*48+1:CD*48)=fluxW.NetRl;
