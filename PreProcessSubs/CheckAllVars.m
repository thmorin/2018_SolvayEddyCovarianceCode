figure();plot(ubar_1_Concat_Concat);title('ubar_1_Concat_Concat');
figure();plot(ubar_2_Concat_Concat);title('ubar_2_Concat_Concat');
figure();plot(STat8cmOWr_W1_Concat);title('STat8cmOWr_W1_Concat');
figure();plot(STat8cmIM_W1_Concat);title('STat8cmIM_W1_Concat');
figure();plot(STat25cmIM_W1_Concat);title('STat25cmIM_W1_Concat');
