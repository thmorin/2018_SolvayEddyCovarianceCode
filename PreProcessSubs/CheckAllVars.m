    Diffuse_PAR_Concat((CD-1)*48+1:CD*48)=fluxW.Diffuse_PAR;
    e_Concat((CD-1)*48+1:CD*48)=fluxW.e;
    p_vaporSat_bar_Concat((CD-1)*48+1:CD*48)=fluxW.p_vaporSat_bar;
